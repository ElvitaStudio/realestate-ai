LANG_INSTRUCTIONS = {
    "ru": "Отвечай на русском языке.",
    "ua": "Відповідай українською мовою.",
    "en": "Reply in English.",
    "bg": "Отговаряй на български език.",
}


def build_prompt(params: dict, language: str) -> str:
    lang = LANG_INSTRUCTIONS.get(language, LANG_INSTRUCTIONS["ru"])

    return f"""{lang}
Ты тренер по продажам для риелторов. Напиши скрипт квалификации входящего звонка.

Тип объекта который интересует клиент: {params.get("property_type", "")}

Структура скрипта:
1. Приветствие и подтверждение запроса клиента
2. Блок квалификации — 5-7 вопросов для выявления потребности:
   - бюджет
   - сроки
   - приоритеты (район, площадь, этаж)
   - цель покупки (себе / инвестиция)
   - текущая ситуация (уже смотрели что-то?)
3. Переход на просмотр — предложить конкретную дату/время
4. Фиксация договорённостей

Добавляй ремарки [записать ответ], [пауза] где нужно."""
