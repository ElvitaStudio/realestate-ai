LANG_INSTRUCTIONS = {
    "ru": "Отвечай на русском языке.",
    "ua": "Відповідай українською мовою.",
    "en": "Reply in English.",
    "bg": "Отговаряй на български език.",
}

GOAL_MAP = {
    "sell_property": "продажа конкретного объекта недвижимости",
    "find_seller": "поиск продавца (собственника готового продать)",
    "evaluation": "приглашение на бесплатную оценку недвижимости",
}

CLIENT_MAP = {
    "owner": "собственник недвижимости",
    "buyer": "потенциальный покупатель",
    "investor": "инвестор",
}


def build_prompt(params: dict, language: str) -> str:
    lang = LANG_INSTRUCTIONS.get(language, LANG_INSTRUCTIONS["ru"])
    goal = GOAL_MAP.get(params.get("goal", "sell_property"), params.get("goal", ""))
    client = CLIENT_MAP.get(params.get("client_type", "buyer"), params.get("client_type", ""))

    return f"""{lang}
Ты тренер по продажам для риелторов. Напиши полный скрипт холодного звонка.

Цель звонка: {goal}
Тип клиента: {client}

Структура скрипта (обязательно соблюди):
1. Приветствие — представиться, назвать компанию
2. Крючок — одна цепляющая фраза, почему звоним
3. Выявление потребности — 2-3 вопроса
4. Предложение — конкретное и лаконичное
5. Работа с возражением "не интересно" — 1 вариант
6. Договорённость о встрече / следующем шаге

Скрипт должен звучать естественно, не как робот. Добавь ремарки [пауза], [ждать ответ] где нужно."""
